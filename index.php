    var $tableData = $('.js-table-data');
    function requestRes(r) {
        $.ajax({
            url: '/status.php',
            data: { val: r },
            success(data) {
                $(`[data-href="${r}"]`)
                    .empty()
                    .append(`<b class="badge badge-${data ? 'success': 'danger'}">${data ? 'працює': 'лежить'}</b>`);
            }
        })
    }
    function getResult(needRender) {
        var $input = $('#txt-input');
        var resources = String($input.val()).split('\n');
        resources.forEach((myRes, i) => {
            var res = String(myRes).trim();
            if (!res) { return; }
            if (!res.startsWith('http')) { res = 'https://' + res; }
            if (needRender) {
                $tableData.append(`
                    <tr data-res="${res}">
                        <td>${i + 1}</td>
                        <td><a href="${res}" target="_blank" rel="noindex nofollow">${res}</a></td>
                        <td data-href="${res}"><b class="badge badge-secondary">запит в процессі</b></td>
                        <td><a href="https://check-host.net/check-ping?host=${encodeURIComponent(res)}" target="_blank" rel="noindex nofollow">Подробиці</a></td>
                    </tr>
                `);
            } else {
                $(`[data-href="${res}"]`)
                    .empty()
                    .append(`<b class="badge badge-secondary">запит в процессі</b>`);
            }
            requestRes(res);
        });
    }
    $('.js-check').on('click', () => {
        getResult(true);
        const $interval = $('.js-interval');
        if ($interval.val() && $interval.val() > 0) {
            setInterval(getResult, $interval.val() * 1000 * 60);
        }
    });
